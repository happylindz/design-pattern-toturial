class Document {
	public Document() {
	}
	public void create() {
		System.out.println("Created!");
	}
	public void open() {
		System.out.println("Opened!");
	}
	public void close() {
		System.out.println("Closed!");
	}
	public void copy() {
		System.out.println("Copyed!");
	}
	public void paste() {
		System.out.println("Pasted!");
	}
}
